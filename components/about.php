<h3 style="color:black;">En savoir plus sur <b>App</b>erture</h3>
<p style="font-size:20px;">Le repertoire des applications métiers de la ville de Vitry-sur-Seine</p>
<p>Apperture est une application web développée par William-Arno CLEMENT dans le cadre de sa formation en alternance à la Mairie de Vitry-sur-Seine sous le regard de Sylvain RAPIN. Le logiciel est développé en PHP Vanilla et construit à l'aide du modèle MVC. Il est destiné a servir de panorama des applications métiers utilisées au sein de la ville.
</p><p>
Pour plus de renseignements, contacter le développeur à wiyam[at]icloud[point]com ou le chef de projet à sylvain[point]rapin[at]mairie[tiret]vitry94[point]fr.</p>
